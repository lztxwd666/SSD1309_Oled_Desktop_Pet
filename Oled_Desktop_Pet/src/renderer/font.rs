//! 位图字体 —— 支持两种字号。
//!
//! * 5×7（标准）：用于标题、状态栏、桌宠区
//! * 4×6（小号）：用于密集数据面板
//!
//! 可通过 `set_font_data()` 加载自定义字体覆盖默认 5×7 字体。

use std::sync::OnceLock;

/// 摄氏度符号 `°`（5×7）—— 3×3 小圆圈，居顶部，标准比例。
/// ```text
///  .***.
///  *...*
///  .***.
/// ```
const DEGREE_GLYPH: &[u8; 5] = &[0x02, 0x05, 0x05, 0x05, 0x02];
/// 摄氏度符号 `°`（4×6）。
const DEGREE_GLYPH_SMALL: &[u8; 4] = &[0b00110, 0b01001, 0b01001, 0b00110];

/// 上箭头 `↑`（5×7）—— 上传/发送流量指示。
const ARROW_UP_GLYPH: &[u8; 5] = &[0x04, 0x06, 0x7F, 0x06, 0x04];
/// 下箭头 `↓`（5×7）—— 下载/接收流量指示。
const ARROW_DOWN_GLYPH: &[u8; 5] = &[0x10, 0x30, 0x7F, 0x30, 0x10];

// 5×7 尺寸
pub const CHAR_WIDTH: usize = 5;
pub const CHAR_HEIGHT: usize = 7;

// 4×6 尺寸
pub const CHAR_WIDTH_SMALL: usize = 4;
pub const CHAR_HEIGHT_SMALL: usize = 6;

static FONT_OVERRIDE: OnceLock<[u8; 475]> = OnceLock::new();

/// 加载自定义字体（覆盖 5×7 默认）。
pub fn set_font_data(data: [u8; 475]) {
    let _ = FONT_OVERRIDE.set(data);
}

