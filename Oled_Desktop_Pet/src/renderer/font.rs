/// 获取 5×7 字体字符数据。
#[inline]
pub fn glyph(ch: char) -> &'static [u8] {
    if ch == '°' {
        return DEGREE_GLYPH;
    }
    if ch == '↑' {
        return ARROW_UP_GLYPH;
    }
    if ch == '↓' {
        return ARROW_DOWN_GLYPH;
    }
    let idx = ch as usize;
    if !(32..=126).contains(&idx) {
        return &FALLBACK_FONT_DATA[0..5];
    }
    let base = (idx - 32) * 5;
    if let Some(data) = FONT_OVERRIDE.get() {
        &data[base..base + 5]
    } else {
        &FALLBACK_FONT_DATA[base..base + 5]
    }
}

/// 获取 4×6 小号字体字符数据。
#[inline]
pub fn glyph_small(ch: char) -> &'static [u8] {
    if ch == '°' {
        return DEGREE_GLYPH_SMALL;
    }
    let idx = ch as usize;
    if !(32..=126).contains(&idx) {
        return &SMALL_FONT_DATA[0..4];
    }
    let base = (idx - 32) * 4;
    &SMALL_FONT_DATA[base..base + 4]
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn default_a() {
        let g = glyph('A');
        assert_eq!(g, &[0x7E, 0x11, 0x11, 0x11, 0x7E]);
    }

    #[test]
    fn small_digit_0() {
        let g = glyph_small('0');
        assert_eq!(g, &[0x0E, 0x11, 0x11, 0x0E]);
    }

    #[test]
    fn small_font_4_bytes_per_char() {
        for ch in 'A'..='Z' {
            assert_eq!(glyph_small(ch).len(), 4);
        }
    }

    #[test]
    fn degree_works() {
        assert_eq!(glyph('°'), &[0x02, 0x05, 0x05, 0x05, 0x02]);
    }

    #[test]
    fn arrow_up_renders() {
        let g = glyph('↑');
        assert_eq!(g, &[0x04, 0x06, 0x7F, 0x06, 0x04]);
    }

    #[test]
    fn arrow_down_renders() {
        let g = glyph('↓');
        assert_eq!(g, &[0x10, 0x30, 0x7F, 0x30, 0x10]);
    }
}
